package org.fasttrackit.reminderProject.transfer.Reminder;


public class ReminderIdentifier {

    private long id;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        ReminderIdentifier that = (ReminderIdentifier) o;
        return id == that.id;
    }

    @Override
    public int hashCode() {
        return (int) (id ^ (id >>> 32));
    }

    @Override
    public String toString() {
        return "ReminderIdentifier{" +
                "id=" + id +
                '}';
    }
}
